#KEY CONCEPTS: math operators: +, -, *, /, //, %, **

add = 7 + 2
print("Sum: ", add)

subtract = 7 - 2
print("Difference: ", subtract)

multiply = 7 * 2
print("Product: ", multiply)

# One slash will always be a float

Float_divide = 7 / 2
print("Quotient: ", Float_divide)

# Two slashes will always be an integer 

integer_divide = 7 // 2
print("Integer Quotient: ", integer_divide)

modulus = 7 % 2
print("modulus: ", modulus)

exponent = 7 ** 2
print("Exponent: ", exponent)

